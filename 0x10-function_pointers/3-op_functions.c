#include "3-calc.h"
/**
 * op_add - Function to add two int
 * @a: First int input
 * @b: Second int input
 * Return: Sum of both num
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -  Function to subtract two int
 * @a: First int input
 * @b: Second int input
 * Return: Subtraction of two int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function to multi two int
 * @a: First int input
 * @b: Second int input
 * Return: Multi of two int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function to divide two int
 * @a: First int input
 * @b: Second int input
 * Return: Division of two int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod -  Function to modulous two int
 * @a: First int input
 * @b: Second int input
 * Return: Modulous of two int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
